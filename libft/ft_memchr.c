#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*us;

	us = (unsigned char *)s;
	while (n)
	{
		if (*us == (unsigned char)c)
			return (us);
		us++;
		n--;
	}
	return ((void *)0);
}
