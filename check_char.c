#include "ft_printf.h"

int	ft_isflag(int c)
{
	return (c == '-' || c == '+' || c == ' ' || c == '#' || c == '0');
}

int	ft_istype(int c)
{
	return (c == 'c' || c == 's' || c == 'p' || c == 'd' || c == 'i' \
	|| c == 'u' || c == 'x' || c == 'X' || c == '%');
}
